import pandas as pd
import matplotlib.pyplot as plt
import numpy as np

df = pd.read_csv('cs_students.csv')
print("---------------------------------------------------------------------------------------------------------------------------")

fd = df.value_counts()
print(fd)
print("---------------------------------------------------------------------------------------------------------------------------")

fd.plot(kind='bar')
print("---------------------------------------------------------------------------------------------------------------------------")
numerical = df.select_dtypes(include=[np.number])
print(numerical)
print("---------------------------------------------------------------------------------------------------------------------------")
percentile_values = numerical.quantile([0.25, 0.5, 0.75])
print(percentile_values)
print("---------------------------------------------------------------------------------------------------------------------------")
skewness = numerical.skew()
print(skewness)
print("---------------------------------------------------------------------------------------------------------------------------")
kurtosis = numerical.kurt()
print(kurtosis)
# sns.displot(kurtosis,bins=100,log_scale=True)
print("---------------------------------------------------------------------------------------------------------------------------")
correlation = numerical.corr()
print(correlation)
print("---------------------------------------------------------------------------------------------------------------------------")  
